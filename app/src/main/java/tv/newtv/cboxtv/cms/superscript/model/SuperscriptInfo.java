public class SuperscriptInfo {
    private String cornerId;
    private String cornerName;
    private String cornerType;
    private String cornerTitle;
    private String cornerDesc;
    private String cornerImg;

    public String getCornerId() {
        return cornerId;
    }

    public void setCornerId(String cornerId) {
        this.cornerId = cornerId;
    }

    public String getCornerName() {
        return cornerName;
    }

    public void setCornerName(String cornerName) {
        this.cornerName = cornerName;
    }

    public String getCornerType() {
        return cornerType;
    }

    public void setCornerType(String cornerType) {
        this.cornerType = cornerType;
    }

    public String getCornerTitle() {
        return cornerTitle;
    }

    public void setCornerTitle(String cornerTitle) {
        this.cornerTitle = cornerTitle;
    }

    public String getCornerDesc() {
        return cornerDesc;
    }

    public void setCornerDesc(String cornerDesc) {
        this.cornerDesc = cornerDesc;
    }

    public String getCornerImg() {
        return cornerImg;
    }

    public void setCornerImg(String cornerImg) {
        this.cornerImg = cornerImg;
    }
}
